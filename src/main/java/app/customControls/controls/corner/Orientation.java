package app.customControls.controls.corner;

/**
 * Represents the orientation of a {@link javafx.scene.Node}<br>
 * <br>
 * <u><i>Values</i></u> :<br>
 * <ul>
 *     <li>TOP_LEFT</li>
 *     <li>TOP</li>
 *     <li>TOP_RIGHT</li>
 *     <li>RIGHT</li>
 *     <li>BOTTOM_RIGHT</li>
 *     <li>BOTTOM</li>
 *     <li>BOTTOM_LEFT</li>
 *     <li>LEFT</li>
 * </ul>
 * @see BorderLine
 */
public enum Orientation {
    TOP_LEFT,
    TOP,
    TOP_RIGHT,
    RIGHT,
    BOTTOM_RIGHT,
    BOTTOM,
    BOTTOM_LEFT,
    LEFT
}
